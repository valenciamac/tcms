	/**
	 * Store a newly created resource in storage.
	 * POST /accounts
	 *
	 * @return Response
	 */
	public function store()
	{
		$project = new Project;
		$project->project_name = Input::get('projectName');
		$project->location = Input::get('projectLocation');
		$project->save();

		$id = $project->slug;
		return Redirect::route('project.show', ['id' => $id]);
		
	}

	/**
	 * Display the specified resource.
	 * GET /accounts/{id}
	 *
	 * @param  int  $id
	 * @return Response
	 */
	public function show($id)
	{
		
		$addproject = Project::where('id', '=', $id)->get();
		$sitem = StandardDesc::with('standardItem')->get();
		return View::make('users.sysAdmin.projectDesc')->withProject($addproject)->withStandardDesc($sitem);
	}

